    }

    public class CreateSubscriptionDtoValidator : AbstractValidator<CreateSubscriptionDto>
    {
        public CreateSubscriptionDtoValidator()
        {
            RuleFor(x => x.Name)
                .NotEmpty().WithMessage("نام اشتراک ضروری است")
                .Length(2, 100).WithMessage("نام اشتراک باید بین 2 تا 100 کاراکتر باشد");

            RuleFor(x => x.Description)
                .MaximumLength(1000).WithMessage("توضیحات حداکثر باید 1000 کاراکتر باشد")
                .When(x => !string.IsNullOrEmpty(x.Description));

            RuleFor(x => x.StartDate)
                .GreaterThanOrEqualTo(DateTime.UtcNow.Date).WithMessage("تاریخ شروع نمی‌تواند از گذشته باشد");

            RuleFor(x => x.EndDate)
                .GreaterThan(x => x.StartDate).WithMessage("تاریخ پایان باید بعد از تاریخ شروع باشد")
                .When(x => x.EndDate.HasValue);

            RuleFor(x => x.PlanId)
                .GreaterThan(0).WithMessage("پلن ضروری است");

            RuleFor(x => x.OrganizationId)
                .GreaterThan(0).WithMessage("سازمان ضروری است");

            RuleFor(x => x.CustomMonthlyTicketLimit)
                .GreaterThan(0).WithMessage("محدودیت تیکت باید بیشتر از صفر باشد")
                .When(x => x.CustomMonthlyTicketLimit.HasValue);

            RuleFor(x => x.CustomPrice)
                .GreaterThanOrEqualTo(0).WithMessage("قیمت نمی‌تواند منفی باشد")
                .When(x => x.CustomPrice.HasValue);
        }
    }

